    private void user_tableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_user_tableMouseReleased
        // TODO add your handling code here:
        int row = user_table.rowAtPoint(evt.getPoint());
        if (row >= 0 && row < user_table.getRowCount()) {
            user_table.setRowSelectionInterval(row, row);
        } else {
            user_table.clearSelection();
        }
        int rowindex = user_table.getSelectedRow();
        if (rowindex < 0) {
            return;
        }
        updateUser = uc.getUserList().get(rowindex);
        deleteUser = uc.getUserList().get(rowindex);
        if (evt.isPopupTrigger()) {
            PopupUserMenu.show(evt.getComponent(), evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_user_tableMouseReleased

